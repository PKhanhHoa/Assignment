package model;

import java.sql.Date;

/**
 *
 * @author Admin
 */
public class Student {
    private int id;
    private String name;
    private Date dob;
    private boolean gender;
    private int idc;
    private String address;
    private String img;

    public Student() {
    }

    public Student(int id, String name, Date dob, boolean gender, int idc, String address, String img) {
        this.id = id;
        this.name = name;
        this.dob = dob;
        this.gender = gender;
        this.idc = idc;
        this.address = address;
        this.img = img;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Date getDob() {
        return dob;
    }

    public void setDob(Date dob) {
        this.dob = dob;
    }

    public boolean isGender() {
        return gender;
    }

    public void setGender(boolean gender) {
        this.gender = gender;
    }

    public int getIdc() {
        return idc;
    }

    public void setIdc(int idc) {
        this.idc = idc;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }
    
    
}
